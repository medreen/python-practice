#Modulo operator
int_1 = 66
int_2 = 13

mod_int = int_1 % int_2
print(mod_int)

float_1 = 6.6
float_2 = 1.3

print(float_1 % float_2)

#floor division
floor_int = int_1 // int_2
print(floor_int)

print(float_1 // float_2) #returns a float

#exponentiantion
exp_int = int_1 ** int_2
print(exp_int)

print(float_1 ** float_2)

